'''
This program takes two integers and shows addition, multiplication, division, modulus and exponentiation.
'''

a = int(input("Enter first integer: "))
b = int(input("Enter second integer: "))

print("\nResults:")
print("Addition      :", a + b)
print("Multiplication:", a * b)
print("Division      :", a / b)
print("Modulus       :", a % b)
print("Exponent      :", a ** b)
